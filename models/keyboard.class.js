/**
 * A class representing the state of keyboard inputs for game controls.
 */
class Keyboard {
    /**
     * Indicates whether the "Left" arrow key is pressed.
     * @type {boolean}
     */
    LEFT = false;
  
    /**
     * Indicates whether the "Right" arrow key is pressed.
     * @type {boolean}
     */
    RIGHT = false;
  
    /**
     * Indicates whether the "Up" arrow key is pressed.
     * @type {boolean}
     */
    UP = false;
  
    /**
     * Indicates whether the "Down" arrow key is pressed.
     * @type {boolean}
     */
    DOWN = false;
  
    /**
     * Indicates whether the "Space" key is pressed (typically for jumping).
     * @type {boolean}
     */
    SPACE = false;
  
    /**
     * Indicates whether the "D" key is pressed (typically for firing or action).
     * @type {boolean}
     */
    D = false;
  }
  